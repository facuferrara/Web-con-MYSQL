      <h3>Qué es el Sector Educativo del MERCOSUR</h3><br><br>
      <p> 
        El Sector Educativo del MERCOSUR es un espacio de coordinación de las políticas educativas que reúne a los países miembros y asociados del MERCOSUR, desde diciembre de 1991, cuando el Consejo del Mercado Común (CMC), a través de la Decisión 07/91, creó la Reunión de Ministros de Educación del MERCOSUR (RME). <br><br>
        Con el tiempo, la Reunión de Ministros creó otras instancias para apoyar el Sector. En 2001, a través de la Decisión CMC 15/01, se aprobó la "estructura organizativa del Sector Educativo del MERCOSUR", que crea el Comité de Coordinación Regional, las Comisiones Regionales Coordinadoras de Área (Básica, Tecnológica y Superior) y el Comité Gestor del Sistema de Información y Comunicación. En 2005 se creó el Comité Asesor del Fondo Educativo del MERCOSUR. En 2006, se creó la Red de Agencias Nacionales de Acreditación, y en 2011 se creó la Comisión Regional Coordinadora de Formación Docente. Además de estas, hay otras instancias, temporales y permanentes, que gestionan acciones específicas. <br><br>
        A través de la negociación de políticas públicas y la elaboración y ejecución de programas y proyectos conjuntos, el Sector Educativo del MERCOSUR busca la integración y el desarrollo de la educación en toda la región del MERCOSUR y países asociados. <br><br>

        VISIÓN <br><br>
        Ser un espacio regional donde se brinda y garantiza una educación con equidad y calidad, caracterizado por el conocimiento recíproco, la interculturalidad, el respeto a la diversidad, la cooperación solidaria, con valores compartidos que contribuyan al mejoramiento y democratización de los sistemas educativos de la región y a generar condiciones favorables para la paz, mediante el desarrollo social, económico y humano sustentable. <br><br>

        MISIÓN <br><br>
        Conformar un espacio educativo común, a través de la concertación de políticas que articulen la educación con el proceso de integración del MERCOSUR, estimulando la movilidad, el intercambio y la formación de una identidad y ciudadanía regional, con el objeto de lograr una educación de calidad para todos, con atención especial a los sectores más vulnerables en un proceso de desarrollo con justicia social y respeto a la diversidad cultural de los pueblos de la región. <br><br>

