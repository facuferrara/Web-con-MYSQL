         <h3>Día del MERCOSUR 2022: "Artesanía. La cerámica como patrimonio cultural"</h3><br><br>
         <p> En el marco de la conmemoración del Día del MERCOSUR el 26 de marzo de cada año, la Presidencia Pro Tempore del bloque, a cargo de Paraguay en el primer semestre del año 2022, propone el tema “Artesanía: la cerámica como patrimonio cultural” y el abordaje pedagógico para el desarrollo en todos los niveles y modalidades del sector educativo. <br><br>
   
           Con esta propuesta se pretende el fortalecimiento del legado cultural en los países miembros del MERCOSUR, como símbolo representativo de la diversidad en el mundo, y la promoción de la historia de los pueblos, de valores y de la identidad a través de la artesanía. <br><br>
           En ese sentido, cada país podrá optar y seleccionar las estrategias más adecuadas a fin de trabajar las actividades propias de su contexto y realidad, basadas en el tema propuesto, pudiendo desarrollarlas en la escuela, en la casa y/o en la comunidad, utilizando variados recursos que ayuden a conocer, recuperar, vivenciar, revivir historias, prácticas y valores. <br><br>
           Acceda <a href="https://drive.google.com/file/d/11-BHFaKwZEzx9l5cNXhzlzNhCN7TV0Fe/view">AQUÍ</a> a los materiales propuestos por la PPT Paraguay para trabajar en las aulas el Día del MERCOSUR 2022.
