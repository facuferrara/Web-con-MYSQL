      El Banco de Datos Terminológicos del Sector Educativo del MERCOSUR (BDT-SEM) es un repositorio que reúne la terminología propia del área de la Educación. Los términos son propuestos y consensuados en el ámbito de las reuniones del Grupo de Trabajo de Terminología del Sector Educativo del MERCOSUR (GTT-SEM).

Este BDT se ha venido implementando desde el año 2007, habiéndose armonizado a la fecha varios centenares de términos disponibles en español y en portugués, considerando las peculiaridades léxicas y semánticas de cada uno de los países miembros. Se ha logrado contar, de este modo, con una cobertura significativa de términos que comprende los distintos niveles educativos como: Educación Inicial, Educación Escolar Básica del 1°, 2°y 3° ciclos, Educación Media Científico y Técnico-Profesional de la Educación Regular, Educación no Formal, Educación Permanente y Educación Superior.

Es importante destacar que el BDT está sostenido por una aplicación web para la gestión de lenguajes documentales, denominada TEMATRES. La configuración que sostiene al BDT fue diseñada a medida para este emprendimiento.
