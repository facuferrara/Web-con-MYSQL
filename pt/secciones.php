         <h3>Funcionamiento</h3><br><br>
         <p> 
         Frente à complexidade das tarefas planejadas, é necessário institucionalizar nos países estruturas permanentes especializadas na gestão dos programas do Setor Educacional do Mercosul - SEM, que colaborem com a Presidência pro Tempore, se relacionem com suas contrapartes nos países e realizem as tarefas de vinculação, assessoramento, seguimento e difusão das atividades demandadas pelo Comitê Coordenador Regional - CCR. Para otimizar a estrutura e seu funcionamento considera-se conveniente: <br><br>

         a. estabelecer mecanismos para a incorporação de novos países associados às atividades do SEM; <br>
         b. melhorar a capacidade de relacionamento do SEM com outros órgãos do MERCOSUL, considerando: <br>
         - a necessidade de que as resoluções da Reunião de Ministros da Educação - RME sejam sistematicamente incluídas nas agendas dos órgãos decisórios do MERCOSUL (Grupo Mercado Comum - GMC e Conselho Mercado Comum - CMC);<br>
         - a importância da interação horizontal com outras reuniões de Ministros da região;<br>
         - a importância de aprofundar os vínculos com o Parlamento do MERCOSUL, o Fórum de Consulta e Concertação Política (FCCP), o Subgrupo de Trabalho nº 10 (SGT 10) e com o Comitê de Cooperação Técnica (CCT). <br>
         - a necessidade de estimular a conformação de redes e uma estreita vinculação entre os níveis administrativos e de gestão dos sistemas educacionais dos países membros e associados da região; <br>
         - a necessidade de fortalecer a coordenação a nível de MERCOSUL nas relações com organismos internacionais, regionais, sub-regionais e outros países. <br><br>

         Quanto à estrutura do Setor Educacional do MERCOSUL, para que seja dinâmico e flexível, se consideram três funções principais:<br><br>

         Políticas (objetivos estratégicos)<br>
         Técnicas (áreas e linhas de ação)<br>
         De execução (elaboração e execução de projetos)<br>
         Estas funções estarão coordenadas pelas seguintes instâncias orgânicas, de acordo com o Regulamento Interno do SEM, aprovado pela Resolução GMC n. 28/01. <br>

         Com função política enumeram-se as seguintes instâncias: <br>

         1. Reunião de Ministros de Educação do MERCOSUL (RME): constitui a instância orgânica superior do Setor Educacional do MERCOSUL, responsável por adotar Acordos sobre os aportes que a gestão educacional possa promover ao desenvolvimento das políticas do MERCOSUL (CMC/DEC. 7/91). <br>

         2. Comitê Coordenador Regional (CCR): é o órgão assessor da Reunião de Ministros que propõe políticas de integração e cooperação na área educacional e coordena o desenvolvimento das ações do SEM. Dessa forma, sua tarefa implica o desenvolvimento de uma série de competências que contenham as seguintes funções: <br>

         a) Atuar como canal de comunicação orgânica do SEM com a estrutura institucional do MERCOSUL, em especial com a RME;
         b) Desenvolver e executar planos, projetos e ações encomendadas pela RME;
         c) Elevar propostas e iniciativas à RME;
         d) Promover a articulação com organismos internacionais de cooperação e financiamento, agências especializadas, instituições e atores da sociedade civil;
         e) Propor, aprovar e avaliar os programas, projetos e ações, estabelecendo as prioridades e as responsabilidades, em função dos objetivos estratégicos estabelecidos;
         f) Identificar os mecanismos de financiamento e as fontes de recursos que permitam a implementação dos programas e projetos prioritários do SEM;
         g) Elaborar um Programa Anual e um calendário de atividades no marco do Plano vigente;
         h) Propor à RME a criação de novas instâncias necessárias para o cumprimento de suas atribuições;
         i) Criar, em caráter transitório, instâncias assessoras ou de trabalho para o desempenho de uma função específica;
         j) Supervisionar, monitorar e avaliar o desenvolvimento do Plano SEM;
         k) Supervisionar e avaliar o funcionamento das diversas instâncias do SEM;
         l) Difundir os avanços do processo de integração e dos resultados dos estudos e projetos realizados;
         m) Informar e prestar contas à RME sobre o trabalho realizado;
         n) Resolver situações não contempladas no Regulamento no seu âmbito de atuação;
         o) Cumprir todas as demais funções encomendadas pela RME.

         Cabe destacar que de forma semestral e rotativa, por ordem alfabética, cada Estado Parte integrante do MERCOSUL exerce a Presidência pro Tempore. Durante esse período os Ministérios de Educação têm a seu cargo o exercício da Secretaria pro Tempore, órgão de apoio operacional que se encarrega de manter a comunicação fluida e permanente com as delegações dos países integrantes, organizar e coordenar as reuniões do Setor, arquivar a documentação oficial e remeter ao Ministério de Relações Exteriores respectivo a documentação procedente da Reunião de Ministros com o objetivo de serem consideradas pelo Grupo Mercado Comum e Conselho Mercado Comum.

         Na estrutura das funções técnicas, encontram-se as seguintes instâncias:

         Comissões Regionais Coordenadoras de Áreas (CRCA): são instâncias assessoras do CCR, encarregadas de propor as linhas estratégicas e seus mecanismos de implementação tendo em conta os objetivos estratégicos estabelecidos pelo Setor Educacional do MERCOSUL.

         Sua tarefa consiste:

         a) Planejar e implementar as linhas de ação;
         b) Propor a criação de instâncias de trabalho para o melhor desenvolvimento de suas funções;
         c) Analisar e avaliar projetos;
         d) Gerir e executar projetos.

         Cabe esclarecer que as CRCA terão a seu cargo elaborar o perfil do projeto, analisar sua visibilidade e elevar a proposta ao CCR, que identificará as fontes de financiamento. Para isso, faz-se necessário um trabalho de coordenação e comunicação flexível que possibilite a retroalimentação. A elaboração final do projeto será de responsabilidade dos grupos de trabalho.

         Comitê Assessor do FEM (CAFEM): unidade que assessora a administração financeira do fundo do SEM.

         No âmbito das funções de execução, enumeramos as seguintes instâncias:

         Grupos de Trabalho (GT): são subsistemas especializados que assessoram o CCR ou uma CRCA. Contam com um funcionamento permanente e tem como objetivo projetar e/ou realizar uma ou várias tarefas relacionadas a um tema. A criação dos GTs deve ser submetida ao CCR e aprovada pela RME.

         Grupo Gestor de Projetos (GGP): são instâncias temporais específicas convocadas ad hoc pelo CCR por iniciativa própria ou por sugestão das Comissões Coordenadores de Área para definir perfis e desenvolver os projetos aprovados no âmbito do SEM/FEM. A elaboração final do projeto estará a cargo dos GGPs, que se reportarão e se comunicarão diretamente com a CRCA.

         Comissão Ad Hoc: instância de trabalho especial criada com caráter temporário com a finalidade de analisar uma temática específica de interesse emergente e considerada de importância para o SEM. A duração da mesma se estende até a conclusão das atividades e finalidades para as quais foi criada. A mesma será formada, a princípio, por funcionários de cada Estado parte.

         Comitê de Gestão: são unidades que administram instâncias específicas relacionadas a processos para um funcionamento eficiente do Setor.

         Comitê Gestor do Sistema de Informação e Comunicação (CGSIC): atende os requerimentos de comunicação, difusão do conhecimento, informação e trabalho colaborativo no âmbito do SEM em qualquer nível, utilizando tecnologias de comunicação social e informação como recursos para cumprir suas funções. Este sistema será administrado por um Comitê que desenha, gere e administra a informação do SEM de acordo com as necessidades de suas distintas instâncias.

         Algumas de suas tarefas serão:

         a) Desenvolver sítio web, plataformas de comunicação, links com os diferentes programas do setor e ambientes colaborativos;
         b) Manter atualizados os dados, documentos de trabalho, atas e anexos das diferentes reuniões;
         c) Difundir as convocatórias, notícias, resoluções do SEM e publicações digitais;
         d) Desenhar e elaborar indicadores e harmonização de termos.<br><br>
   
