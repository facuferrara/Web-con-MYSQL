         <h3>O que é o Setor Educacional do Mercosul</h3><br><br>
         <p> 
            O Setor Educacional do MERCOSUL é um espaço de coordenação das políticas educacionais que reúne países membros e associados ao MERCOSUL, desde dezembro de 1991, quando o Conselho do Mercado Comum (CMC) criou, através da Decisão 07/91, a Reunião de Ministros de Educação do MERCOSUL (RME). <br><br>

            Ao longo do tempo, a Reunião de Ministros criou outras instâncias de apoio ao Setor. Em 2001, o CMC, através da decisão 15/01, aprovou a “Estrutura orgânica do Setor Educacional do MERCOSUL”, onde se cria o Comitê Coordenador Regional e as Comissões Coordenadoras de Área (Básica, Tecnológica e Superior) e o Comitê Gestor do Sistema de Informação e Comunicação. Em 2005 foi criado o Comitê Assessor do Fundo Educacional do MERCOSUL. Em 2006, foi criada a Rede de Agências Nacionais de Acreditação, e em 2011, foi criada a Comissão Regional Coordenadora de Formação Docente. Além destes, existem outras instâncias, provisórias e permanentes, que gerenciam ações específicas. <br><br>

            Através da negociação de políticas públicas e da elaboração e implementação de programas e projetos conjuntos, o Setor Educacional do MERCOSUL busca a integração e o desenvolvimento da educação em toda a região do MERCOSUL e países associados. <br><br>
   
            VISÃO <br><br>
            Ser un espacio regional donde se brinda y garantiza una educación con equidad y calidad, caracterizado por el conocimiento recíproco, la interculturalidad, el respeto a la diversidad, la cooperación solidaria, con valores compartidos que contribuyan al mejoramiento y democratización de los sistemas educativos de la región y a generar condiciones favorables para la paz, mediante el desarrollo social, económico y humano sustentable. <br><br>
   
            MISIÓN <br><br>
            Ser um espaço regional onde se prevê e garante uma educação com equidade e qualidade, caracterizada pelo conhecimento recíproco, a interculturalidade, o respeito à diversidade e à cooperação solidária, com valores compartilhados que contribuem para a melhoria e democratização dos sistemas educacionais da região e oferecer condições favoráveis para a paz, por meio do desenvolvimento social, econômico e humano sustentável. <br><br>
   
            MISSÃO <br><br>
            Formar um espaço educacional comum, por meio da coordenação de políticas que articulem a educação com o processo de integração do MERCOSUL, estimulando a mobilidade, o intercâmbio e a formação de uma identidade e cidadania regional, com o objetivo de alcançar uma educação de qualidade para todos, com atenção especial aos setores mais vulneráveis, em um processo de desenvolvimento com justiça social e respeito à diversidade cultural dos povos da região. <br><br>

